SELECT 
    П.фамилия, 
    П.имя, 
    П.отчество, 
    П.адрес
FROM Подписки ПК
JOIN Подписчики П ON ПК.id_подписчика = П.id
JOIN Издания И ON ПК.id_издания = И.id
WHERE И.название = 'Газета "Правда"'  
ORDER BY П.фамилия;

SELECT 
    П.фамилия,
    П.имя,
    П.отчество,
    И.название as издание,
    ПК.месяцев,
    (И.цена_месяц * ПК.месяцев) as сумма
FROM Подписки ПК
JOIN Подписчики П ON ПК.id_подписчика = П.id
JOIN Издания И ON ПК.id_издания = И.id
ORDER BY П.фамилия, сумма DESC;


